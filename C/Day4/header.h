//int 로 함수의 반환타입을 선언//
//뒤에 함수를 정의함//

int sum (int a, int b)
{
    int value = a+b;
    return value;
}

int subs (int a, int b)
{
    int value = a-b;
    return value;
}

int multi (int a, int b)
{
    int value = a*b;
    return value;
}

int divi (int a, int b)
{
    int value = a/b; 
    return value;
}