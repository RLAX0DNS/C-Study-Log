   //%f - 실수; %c - 문자; 문자가 대응하는 아스키 문자열을 출력//
   //%s - 문자열//
   //%u - 부호없는 10진수 정수; 문자가 대응하는 10진수 정수의 값의 부호를 고려 안 하고 출력; 출력 크기는 4바이트; 때문에 숫자가 바뀔수 있음 주의//
